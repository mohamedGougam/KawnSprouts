export function getShopItem(id: string): ShopItem | undefined {
  return SHOP_CATALOG.find((i) => i.id === id);
}

export function getCatalogByCategory(category: ShopCategory, ownedIds: Set<string>): ShopItem[] {
  if (category === 'owned') {
    return SHOP_CATALOG.filter((i) => ownedIds.has(i.id));
  }
  if (category === 'special') {
    return SHOP_CATALOG.filter((i) => i.category === 'special' || i.seasonal);
  }
  return SHOP_CATALOG.filter((i) => i.category === category);
}

/** Deterministic daily featured items */
export function getFeaturedItems(date = new Date()): ShopItem[] {
  const day = date.toISOString().slice(0, 10);
  const seed = day.split('-').reduce((a, b) => a + parseInt(b, 10), 0);
  const pool = SHOP_CATALOG.filter((i) => i.featured !== false);
  const picks: ShopItem[] = [];
  for (let i = 0; i < 3; i++) {
    picks.push(pool[(seed + i * 7) % pool.length]);
  }
  return picks;
}
